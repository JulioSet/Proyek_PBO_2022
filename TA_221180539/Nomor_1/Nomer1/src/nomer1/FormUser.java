      public void print() {
            DefaultListModel model = new DefaultListModel();
            for (int i = 0; i < us.getList_lagu().size(); i++) {
                  model.addElement(us.kalimat(i));
            }
            daftar.setModel(model);
      }
      
      public void reset_field() {
            daftar_judul.setText("");
            angka.setValue(0);
      }

