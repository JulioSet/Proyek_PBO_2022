      private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
            String nama = daftar_judul.getText();
            
            if (!nama.equals("")) {
                 us.tambah_lagu(nama);
            } else {
                  JOptionPane.showMessageDialog(null, "Judul lagu tidak boleh kosong !");
            }
            print();
            reset_field();
      }//GEN-LAST:event_addActionPerformed

      private void topupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_topupActionPerformed
            int input_saldo = Integer.parseInt(angka.getValue().toString());
            
            if (input_saldo > 0 ) {
                  us.tambah_saldo(input_saldo);
            } else {
                  JOptionPane.showMessageDialog(null, "TopUp saldo harus diatas 0 !");
            }
            saldo.setText("Saldo Rp " + us.getTopup());
            reset_field();
      }//GEN-LAST:event_topupActionPerformed

