      private void b_dengerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_dengerActionPerformed
            int idx = listLagu.getSelectedIndex();
            us.tambah_puter(idx);
            judul.setText("-");
            muter.setText("-");
            print();
      }//GEN-LAST:event_b_dengerActionPerformed

      private void listLaguMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listLaguMouseClicked
            int idx = listLagu.getSelectedIndex();
            
            if (idx != -1) {
                  mode = "on";
                  judul.setText(us.getList_lagu().get(idx).getJudul());
                  int temp = us.getList_lagu().get(idx).getPutar();
                  muter.setText(String.valueOf(temp));
                  button();
            }
      }//GEN-LAST:event_listLaguMouseClicked

