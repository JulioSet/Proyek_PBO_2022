      public void print() {
            DefaultListModel model = new DefaultListModel();
            for (int i = 0; i < us.getList_lagu().size(); i++) {
                  model.addElement(us.kalimat(i));
            }
            listLagu.setModel(model);
      }
      
      public void button() {
            if (mode.equals("off")) {
                  b_denger.setEnabled(false);
                  clear.setEnabled(false);
            } else if (mode.equals("on")) {
                  b_denger.setEnabled(true);
                  clear.setEnabled(true);
            }
      }

